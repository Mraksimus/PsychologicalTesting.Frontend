const TestingPage: React.FC = () => {
    const { testId } = useParams<{ testId: string }>();
    const navigate = useNavigate();

    const testIdNum = parseInt(testId || '1');
    const testData = testsData[testIdNum];

    const [currentQuestionIndex, setCurrentQuestionIndex] = useState(() => {
        const savedProgress = localStorage.getItem(`testProgress_${testIdNum}`);
        if (savedProgress) {
            try {
                const progressData = JSON.parse(savedProgress);
                const isDataFresh = Date.now() - progressData.timestamp < 24 * 60 * 60 * 1000;
                if (isDataFresh && progressData.testId === testIdNum) {
                    return progressData.currentQuestionIndex;
                }
            } catch (error) {
                console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–∏ –ø—Ä–æ–≥—Ä–µ—Å—Å–∞:', error);
            }
        }
        return 0;
    });

    const [userAnswers, setUserAnswers] = useState<{ [key: number]: number }>(() => {
        const savedProgress = localStorage.getItem(`testProgress_${testIdNum}`);
        if (savedProgress) {
            try {
                const progressData = JSON.parse(savedProgress);
                const isDataFresh = Date.now() - progressData.timestamp < 24 * 60 * 60 * 1000;
                if (isDataFresh && progressData.testId === testIdNum) {
                    return progressData.userAnswers;
                }
            } catch (error) {
                console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–∏ –ø—Ä–æ–≥—Ä–µ—Å—Å–∞:', error);
            }
        }
        return {};
    });

    const currentQuestion = testData.questions[currentQuestionIndex];
    const totalQuestions = testData.questions.length;
    const progress = ((currentQuestionIndex + 1) / totalQuestions) * 100;

    useEffect(() => {
        const progressData = {
            testId: testIdNum,
            currentQuestionIndex,
            userAnswers,
            timestamp: Date.now()
        };
        localStorage.setItem(`testProgress_${testIdNum}`, JSON.stringify(progressData));
    }, [currentQuestionIndex, userAnswers, testIdNum]);

    if (!testData) {
        return <div>–¢–µ—Å—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω</div>;
    }

    const handleSelectAnswer = (answerIndex: number) => {
        setUserAnswers({
            ...userAnswers,
            [currentQuestion.id]: answerIndex
        });
    };

    const handleNextQuestion = () => {
        if (userAnswers[currentQuestion.id] === undefined) {
            alert('–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤—ã–±–µ—Ä–∏—Ç–µ –æ—Ç–≤–µ—Ç');
            return;
        }

        if (currentQuestionIndex < totalQuestions - 1) {
            setCurrentQuestionIndex(currentQuestionIndex + 1);
        } else {
            calculateAndNavigateToResults();
        }
    };

    const handlePreviousQuestion = () => {
        if (currentQuestionIndex > 0) {
            setCurrentQuestionIndex(currentQuestionIndex - 1);
        }
    };

    const handleRestartTest = () => {
        if (window.confirm('–í—ã —É–≤–µ—Ä–µ–Ω—ã, —á—Ç–æ —Ö–æ—Ç–∏—Ç–µ –Ω–∞—á–∞—Ç—å —Ç–µ—Å—Ç –∑–∞–Ω–æ–≤–æ? –í–µ—Å—å –≤–∞—à –ø—Ä–æ–≥—Ä–µ—Å—Å –±—É–¥–µ—Ç –ø–æ—Ç–µ—Ä—è–Ω.')) {
            setCurrentQuestionIndex(0);
            setUserAnswers({});
            localStorage.removeItem(`testProgress_${testIdNum}`);
        }
    };

    const calculateAndNavigateToResults = () => {
        localStorage.removeItem(`testProgress_${testIdNum}`);

        let totalScore = 0;
        const maxPossibleScore = totalQuestions * 3;

        testData.questions.forEach(question => {
            const userAnswer = userAnswers[question.id];
            const questionScore = 3 - (userAnswer || 0);
            totalScore += questionScore;
        });

        const percentile = Math.round((totalScore / maxPossibleScore) * 100);

        const resultsData = {
            testId: testIdNum,
            testTitle: testData.title,
            category: testData.category,
            percentile: percentile,
            userAnswers
        };

        navigate(`/result`, { state: resultsData });
    };

    const hasSavedProgress = Object.keys(userAnswers).length > 0 || currentQuestionIndex > 0;

    return (
        <>
            <Header />
            <Container size="xl" style={{ minHeight: '100vh', padding: '40px 0' }}>
                <div style={{ textAlign: 'center', color: 'white', marginBottom: '40px' }}>
                    <Title order={1}>{testData.title}</Title>
                    <Text size="lg" style={{ color: 'rgba(255,255,255,0.8)' }}>
                        {testData.category}
                    </Text>

                    {hasSavedProgress && (
                        <Badge color="green" variant="light" size="lg" style={{ marginTop: '10px' }}>
                            üîÑ –ü—Ä–æ–≥—Ä–µ—Å—Å –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω
                        </Badge>
                    )}
                </div>

                <Card shadow="md" p="xl" style={{ background: 'rgba(255,255,255,0.95)', borderRadius: '12px' }}>
                    <Stack gap="md" mb="xl">
                        <Group justify="space-between">
                            <Text fw={500}>
                                –í–æ–ø—Ä–æ—Å {currentQuestionIndex + 1} –∏–∑ {totalQuestions}
                            </Text>
                            <Text fw={500} c="dimmed">
                                {Math.round(progress)}%
                            </Text>
                        </Group>
                        <Progress value={progress} size="lg" radius="md" />
                    </Stack>

                    <Stack gap="lg" mb="xl">
                        <div>
                            <Badge size="lg" mb="md" color="violet">
                                –í–æ–ø—Ä–æ—Å {currentQuestionIndex + 1}
                            </Badge>
                            <Title order={2} size="h3" style={{ marginBottom: '30px', color: '#2c3e50' }}>
                                {currentQuestion.text}
                            </Title>
                        </div>

                        <Stack gap="md">
                            {currentQuestion.options.map((option, index) => (
                                <Card
                                    key={index}
                                    p="md"
                                    radius="md"
                                    style={{
                                        border: userAnswers[currentQuestion.id] === index
                                            ? '2px solid #667eea'
                                            : '2px solid #e0e0e0',
                                        background: userAnswers[currentQuestion.id] === index
                                            ? 'linear-gradient(135deg, #667eea 0%, #764ba2 100%)'
                                            : 'white',
                                        cursor: 'pointer',
                                        transition: 'all 0.3s ease',
                                        transform: userAnswers[currentQuestion.id] === index ? 'scale(1.02)' : 'scale(1)'
                                    }}
                                    onClick={() => handleSelectAnswer(index)}
                                    withBorder
                                >
                                    <Group>
                                        <div
                                            style={{
                                                width: '24px',
                                                height: '24px',
                                                borderRadius: '50%',
                                                border: '2px solid ' + (userAnswers[currentQuestion.id] === index ? 'white' : '#ccc'),
                                                display: 'flex',
                                                alignItems: 'center',
                                                justifyContent: 'center',
                                                backgroundColor: userAnswers[currentQuestion.id] === index ? 'white' : 'transparent'
                                            }}
                                        >
                                            {userAnswers[currentQuestion.id] === index && (
                                                <div style={{
                                                    width: '8px',
                                                    height: '8px',
                                                    backgroundColor: '#667eea',
                                                    borderRadius: '50%'
                                                }} />
                                            )}
                                        </div>
                                        <Text
                                            fw={500}
                                            style={{
                                                color: userAnswers[currentQuestion.id] === index ? 'white' : '#333'
                                            }}
                                        >
                                            {option}
                                        </Text>
                                    </Group>
                                </Card>
                            ))}
                        </Stack>
                    </Stack>

                    <Group justify="space-between" mt="xl">
                        <Group>
                            <Button
                                variant="light"
                                disabled={currentQuestionIndex === 0}
                                onClick={handlePreviousQuestion}
                            >
                                ‚Üê –ù–∞–∑–∞–¥
                            </Button>

                            {hasSavedProgress && (
                                <Button
                                    variant="outline"
                                    color="red"
                                    onClick={handleRestartTest}
                                >
                                    üîÑ –ù–∞—á–∞—Ç—å –∑–∞–Ω–æ–≤–æ
                                </Button>
                            )}
                        </Group>

                        <Button
                            gradient={{ from: 'blue', to: 'cyan' }}
                            onClick={handleNextQuestion}
                        >
                            {currentQuestionIndex === totalQuestions - 1 ? '–ó–∞–≤–µ—Ä—à–∏—Ç—å ‚Üí' : '–î–∞–ª–µ–µ ‚Üí'}
                        </Button>
                    </Group>
                </Card>
            </Container>
        </>
    );
};

export default TestingPage;